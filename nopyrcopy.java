class nopyrcopy
{
public static void main(String[] args)
{
System.out.print("\n\n");
int n=5;
for(int i=1;i<=n;i++)
{
  for(int j=1;j<=n-i;j++)
  {
    System.out.print(" ");
  }
  for(int j=1;j<=i;j++)
  {
     System.out.print(i+" ");
  }
     System.out.print("\n\n");
 
}




//main end
}
}